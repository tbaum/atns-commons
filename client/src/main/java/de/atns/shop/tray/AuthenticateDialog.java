    private JTextField login;
    private JPasswordField passwort;
    private JTextField shopName;
    @Inject private final AuthtokenCache authentication;
    private ShopConfiguration currentConfiguration;

// --------------------------- CONSTRUCTORS ---------------------------

    @Inject public AuthenticateDialog(final AuthtokenCache authentication) {
        this.authentication = authentication;

        setTitle("Anmeldung:");
        setContentPane(contentPane);
        setModal(true);
        getRootPane().setDefaultButton(buttonOK);

        buttonOK.addActionListener(new ActionListener() {
            public void actionPerformed(final ActionEvent e) {
                onOK();
            }
        });

        buttonCancel.addActionListener(new ActionListener() {
            public void actionPerformed(final ActionEvent e) {
                onCancel();
            }
        });

// call onCancel() when cross is clicked
        setDefaultCloseOperation(DO_NOTHING_ON_CLOSE);
        addWindowListener(new WindowAdapter() {
            public void windowClosing(final WindowEvent e) {
                onCancel();
            }
        });

// call onCancel() on ESCAPE
        contentPane.registerKeyboardAction(new ActionListener() {
            public void actionPerformed(final ActionEvent e) {
                onCancel();
            }
        }, KeyStroke.getKeyStroke(KeyEvent.VK_ESCAPE, 0), JComponent.WHEN_ANCESTOR_OF_FOCUSED_COMPONENT);

        pack();
        Util.center(AuthenticateDialog.this);
    }

    private void onOK() {
        try {
            authentication.authenticate(currentConfiguration, login.getText(), new String(passwort.getPassword()));
            dispose();
        } catch (RemoteAuthenticationException e) {
            showError("Benutzername / Passwort konnte nicht geprÃ¼ft werden!");
        } catch (RemoteException e) {
            System.err.println(e.getResponse());
            e.printStackTrace();
            showError("Fehler:\n" + e.getResponse());
        }
        currentConfiguration = null;
    }

    private void showError(final String msg) {
        JOptionPane.showMessageDialog(this, msg, "Fehler", JOptionPane.ERROR_MESSAGE);
        setVisible(false);
    }

    private void onCancel() {
        dispose();
        currentConfiguration = null;
    }

    // -------------------------- OTHER METHODS --------------------------

    public void authenticate(final String shopId) {
        shopName.setText("");
        login.setText("");
        passwort.setText("");
        login.requestFocusInWindow();
        
        currentConfiguration = new ShopConfiguration(shopId);

        final String name = currentConfiguration.getName();
        if (name == null || name.length() == 0) {
            showError("Shop mit Kennung '" + shopId + "' nicht konfiguriert!!");
            return;
        }

        shopName.setText(name);

        setVisible(true);
    }

